# Function to load and preprocess both audio and text
def preprocess_audio_and_text(audio_path, text_path):
    # Load and preprocess the audio
    audio, sr = librosa.load(audio_path, sr=None)  # Load audio with original sample rate
    mfcc = librosa.feature.mfcc(y=audio, sr=sr, n_mfcc=13)  # Extract 13 MFCC features
    mfcc_delta = librosa.feature.delta(mfcc)  # First order delta (derivative)
    mfcc_delta2 = librosa.feature.delta(mfcc, order=2)  # Second order delta (second derivative)
    mfcc_features = np.concatenate([mfcc, mfcc_delta, mfcc_delta2], axis=0)  # Concatenate MFCCs with deltas
    mfcc_features = np.mean(mfcc_features.T, axis=0)  # Average over time frames

    # Load and preprocess the text
    with open(text_path, 'r') as file:
        text = file.read().strip()  # Read and clean the text

    return mfcc_features, text  # Return both audio and text features

