# Function to load and preprocess the audio
def preprocess_audio(audio_path):
    audio, sr = librosa.load(audio_path, sr=None)  # Load audio with original sample rate
    # Extract MFCCs (Mel-frequency cepstral coefficients) as features
    mfcc = librosa.feature.mfcc(y=audio, sr=sr, n_mfcc=13)  # Corrected to use keyword arguments
    mfcc = np.mean(mfcc.T, axis=0)  # Averaging over time axis (frames)
    return mfcc

